#[derive(Clone, Hash, PartialEq, Eq)]
struct Sudoku {
    board: [u8; 81],
}

impl Debug for Sudoku {
    fn fmt(&self, f: &mut Formatter<'_>) -> std::fmt::Result {
        write!(f, "{}", self)
    }
}

impl Sudoku {
    fn solve(self) -> Result<(Sudoku, usize), String> {
        let mut number_steps = 0usize;
        let mut frontier = Vec::with_capacity(10);
        let mut visited = HashSet::with_capacity(80);
        frontier.push(self);
        loop {
            number_steps += 1;
            if let Some(selected) = frontier.pop() {
                if selected.is_solved() {
                    return Ok((selected, number_steps));
                }
                let extended = selected.extend();
                visited.insert(selected);
                for node in extended {
                    if !frontier.contains(&node) && !visited.contains(&node) {
                        frontier.push(node);
                    }
                }
            } else {
                return Err(format!("Could not solve Sudoku in {} steps", number_steps));
            }
        }
    }

    fn extend(&self) -> Vec<Self> {
        if let Some(first_zero) = self.board.iter().position(|x| x == &0) {
            let mut constraints = HashSet::from([1u8, 2, 3, 4, 5, 6, 7, 8, 9]);
            for pos_to_check in TABLE[first_zero] {
                constraints.remove(&self.board[pos_to_check as usize]);
            }
            let mut new_nodes = Vec::with_capacity(constraints.len());
            for left in constraints {
                let mut new = self.clone();
                new.board[first_zero] = left;
                new_nodes.push(new);
            }
            new_nodes
        } else {
            vec![]
        }
    }

    fn is_solved(&self) -> bool {
        !self.board.contains(&0)
    }
}

impl Display for Sudoku {
    fn fmt(&self, f: &mut Formatter<'_>) -> std::fmt::Result {
        writeln!(f, "-------------------------")?;
        for square_y in 0..3usize {
            for line in 0..3usize {
                write!(f, "| ")?;
                for square_x in 0..3usize {
                    for column in 0..3usize {
                        write!(
                            f,
                            "{} ",
                            &self.board[square_y * 27 + line * 9 + 3 * square_x + column]
                        )?;
                    }
                    write!(f, "| ")?;
                }
                writeln!(f, "")?;
            }
            writeln!(f, "-------------------------")?;
        }
        Ok(())
    }
}

impl FromStr for Sudoku {
    type Err = String;

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        if s.len() != 81 {
            return Err("Not a valid board".into());
        }
        let mut board = [0u8; 81];
        s.chars().enumerate().try_for_each(|(idx, nbr)| {
            if let Some(x) = nbr.to_digit(10).filter(|&x| x < 10) {
                board[idx] = x as u8;
                Ok(())
            } else {
                Err(format!("Not a valid character at {}", idx))
            }
        })?;
        Ok(Sudoku { board })
    }
}

